package metodoExAula;

public class Calculadora {

	public static int soma(int valor ,int valor2) {
		return (valor+valor2);
	}
	
	public static int divisao(int valor,int valor2) {
		return (valor/valor2);
	}
	
	public static int multiplicao(int valor,int valor2) {
		return (valor*valor2);
	}
	
	public static int subtracao(int valor,int valor2) {
		return (valor-valor2);
	}
	
	public static int rest_Divisao(int valor,int valor2) {
		return (valor%valor2);
	}
}
